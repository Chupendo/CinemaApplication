package com.tokioschool.filmapp.enums;

/**
 * Enumeración que representa los tipos de artistas disponibles en la aplicación.
 *
 * Esta enumeración define los diferentes tipos de artistas que pueden estar
 * asociados a una película, como DIRECTOR o ACTOR.
 *
 * Es utilizada para clasificar y gestionar los roles de los artistas en el sistema.
 *
 * @author andres.rpenuela
 * @version 1.0
 */
public enum TYPE_ARTIS_DTO {
    /**
     * Tipo de artista que representa a un director.
     */
    DIRECTOR,

    /**
     * Tipo de artista que representa a un actor.
     */
    ACTOR
}